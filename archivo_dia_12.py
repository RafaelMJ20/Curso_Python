# Entero
edad = 21
print('Edad: ', edad)

# Número con punto flotante
altura = 1.54
print('Altura: ', altura)

# Cadena de texto
nombre = 'Ana'
print('Nombre: ', nombre)

# Tipo boolean
es_estudiante = False
print('Es estudiante? ', es_estudiante)

# None: ausencia de valor
direccion = None
print('Dirección: ', direccion)
